    private void mailMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mailMousePressed
        if (mail.getText().equals("Introduce un correo elecrónico")){
            mail.setText("");
            mail.setForeground(Color.black);
        }
        
        if (nombreUsuario.getText().isEmpty()){
            nombreUsuario.setText("Introduce un nombre de usuario");
            nombreUsuario.setForeground(Color.gray);
        }
        
        if (String.valueOf(pass.getPassword()).isEmpty() && String.valueOf(repetirPass.getPassword()).isEmpty()){
            pass.setText("********");
            repetirPass.setText("********");
            pass.setForeground(Color.gray);
            repetirPass.setForeground(Color.gray);
        }
    }//GEN-LAST:event_mailMousePressed

    private void nombreUsuarioMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_nombreUsuarioMousePressed
        if (nombreUsuario.getText().equals("Introduce un nombre de usuario")){
            nombreUsuario.setText("");
            nombreUsuario.setForeground(Color.black);
        }
        
        if (mail.getText().isEmpty()){
            mail.setText("Introduce un correo electrónico");
            mail.setForeground(Color.gray);
        }
        
        if (String.valueOf(pass.getPassword()).isEmpty() && String.valueOf(repetirPass.getPassword()).isEmpty()){
            pass.setText("********");
            repetirPass.setText("********");
            pass.setForeground(Color.gray);
            repetirPass.setForeground(Color.gray);
        }
    }//GEN-LAST:event_nombreUsuarioMousePressed

    private void passMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_passMousePressed
        if (String.valueOf(pass.getPassword()).equals("********")){
            pass.setText("");
            pass.setForeground(Color.black);
        }
        
        if (mail.getText().isEmpty()){
            mail.setText("Introduce un correo electrónico");
            mail.setForeground(Color.gray);
        }
        
        if (nombreUsuario.getText().isEmpty()){
            nombreUsuario.setText("Introduce un nombre de usuario");
            nombreUsuario.setForeground(Color.gray);
        }
        
        if (String.valueOf(repetirPass.getPassword()).isEmpty()){
            repetirPass.setText("********");
            repetirPass.setForeground(Color.gray);
        }
    }//GEN-LAST:event_passMousePressed

